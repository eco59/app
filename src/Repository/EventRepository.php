    /**
     * Récupère les événements approuvés et non expirés.
     *
     * @return Event[]
     */
    public function findApprovedAndActiveEvents(): array
    {
        $qb = $this->createQueryBuilder('e')
            ->where('e.status = :status')
            ->andWhere('e.date_heure_fin > :now')
            ->setParameter('status', 'approved')
            ->setParameter('now', new \DateTime());

        return $qb->getQuery()->getResult();
    }
